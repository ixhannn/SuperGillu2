security definer
set search_path = public
as $$
begin
  insert into public.storage_alerts (
    couple_id,
    feature,
    alert_type,
    severity,
    fingerprint,
    title,
    details,
    status,
    first_seen_at,
    last_seen_at,
    occurrence_count
  )
