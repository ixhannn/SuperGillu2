create table if not exists public.media_assets (
  id uuid primary key default gen_random_uuid(),
  couple_id uuid not null,
  owner_user_id uuid,
  source_table text not null,
  logical_row_id text not null,
  item_id text not null,
  feature text not null,
  asset_role text not null,
  r2_key text not null,
  byte_size bigint not null default 0 check (byte_size >= 0),
  mime_type text not null,
  checksum_sha256 text not null,
  status text not null default 'pending' check (status in ('pending', 'ready', 'missing', 'orphaned', 'failed', 'deleting')),
  expires_at timestamptz,
  metadata jsonb not null default '{}'::jsonb,
  upload_started_at timestamptz not null default now(),
  ready_at timestamptz,
  last_verified_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists media_assets_scope_role_uidx
  on public.media_assets (couple_id, source_table, logical_row_id, asset_role);

create unique index if not exists media_assets_r2_key_uidx
  on public.media_assets (r2_key);

create index if not exists media_assets_couple_feature_idx
  on public.media_assets (couple_id, feature, status, created_at desc);

create index if not exists media_assets_status_idx
  on public.media_assets (status, updated_at desc);

create table if not exists public.storage_events (
  id bigint generated by default as identity primary key,
  couple_id uuid,
  feature text,
  severity text not null default 'info' check (severity in ('info', 'warning', 'error', 'critical')),
  event_type text not null,
  r2_key text,
  source_table text,
  logical_row_id text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists storage_events_created_idx
  on public.storage_events (created_at desc);

create index if not exists storage_events_type_idx
  on public.storage_events (event_type, created_at desc);

create index if not exists storage_events_couple_idx
  on public.storage_events (couple_id, created_at desc);

create table if not exists public.storage_alerts (
  id uuid primary key default gen_random_uuid(),
  couple_id uuid,
  feature text,
  alert_type text not null,
  severity text not null default 'error' check (severity in ('warning', 'error', 'critical')),
  fingerprint text not null unique,
  title text not null,
  details jsonb not null default '{}'::jsonb,
  status text not null default 'open' check (status in ('open', 'resolved')),
  first_seen_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  occurrence_count integer not null default 1
);

create index if not exists storage_alerts_status_idx
  on public.storage_alerts (status, severity, last_seen_at desc);

create index if not exists storage_alerts_couple_idx
  on public.storage_alerts (couple_id, status, last_seen_at desc);

create table if not exists public.storage_metrics_daily (
  metric_date date not null,
  couple_id uuid,
  feature text not null,
  object_count integer not null default 0,
  total_bytes bigint not null default 0,
