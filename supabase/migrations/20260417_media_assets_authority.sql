create or replace function public.storage_audit_repeated_missing_reads(max_rows integer default 100)
returns table (
  couple_id uuid,
  feature text,
  r2_key text,
  occurrences bigint,
  last_seen_at timestamptz
)
language sql
security definer
set search_path = public
as $$
  select
    se.couple_id,
    se.feature,
    se.r2_key,
    count(*) as occurrences,
    max(se.created_at) as last_seen_at
  from public.storage_events se
  where se.event_type = 'media.read_missing'
    and se.created_at >= now() - interval '1 day'
    and coalesce(se.r2_key, '') <> ''
  group by se.couple_id, se.feature, se.r2_key
  having count(*) >= 3
  order by occurrences desc, last_seen_at desc
  limit greatest(max_rows, 1);
$$;

