create or replace function public.storage_console_summary()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_couple_id uuid;
begin
  if auth.uid() is null then
    raise exception 'Authentication required';
  end if;

  v_couple_id := public.ensure_user_couple();

  return jsonb_build_object(
    'couple_id', v_couple_id,
    'usage', coalesce((
      select jsonb_agg(jsonb_build_object(
        'feature', feature,
        'object_count', count(*),
        'total_bytes', coalesce(sum(byte_size), 0),
        'missing_count', count(*) filter (where status = 'missing')
      ) order by feature)
      from public.media_assets
      where couple_id = v_couple_id
        and status in ('pending', 'ready', 'missing')
      group by couple_id
    ), '[]'::jsonb),
    'open_alerts', coalesce((
      select count(*)
      from public.storage_alerts
      where couple_id = v_couple_id
        and status = 'open'
    ), 0),
    'cleanup_backlog', coalesce((
      select count(*)
      from public.media_cleanup_tasks
      where couple_id = v_couple_id
        and status in ('pending', 'processing')
    ), 0)
  );
end;
$$;

